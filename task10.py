import math

# Запрашиваем у пользователя радиус основания и высоту цилиндра
radius = float(input("Введите радиус основания цилиндра в метрах (м): "))
height = float(input("Введите высоту цилиндра в метрах (м): "))

# Проверяем, чтобы радиус и высота были больше нуля
if radius <= 0 or height <= 0:
    print("Радиус и высота должны быть положительными числами.")
else:
    # Вычисляем объем цилиндра
    volume = math.pi * radius**2 * height
    # Выводим результат
    print(f"Объем цилиндра: {volume:.2f} кубических метров")