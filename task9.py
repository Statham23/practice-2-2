# Запрашиваем у пользователя период колебаний
period = float(input("Введите период колебаний в секундах (с): "))

# Проверяем, чтобы период был больше нуля
if period <= 0:
    print("Период должен быть положительным числом.")
else:
    # Вычисляем частоту
    frequency = 1 / period
    # Выводим результат
    print(f"Частота: {frequency} Гц")