# Запрашиваем у пользователя силу и ускорение
force = float(input("Введите силу в ньютонах (N): "))
acceleration = float(input("Введите ускорение в м/с^2: "))

# Проверяем, что ускорение не равно нулю
if acceleration == 0:
    print("Ускорение не может быть равно нулю!")
else:
    # Вычисляем массу
    mass = force / acceleration
    # Выводим результат
    print(f"Масса: {mass} кг")