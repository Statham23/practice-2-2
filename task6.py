# Запрашиваем у пользователя массу, высоту и ускорение свободного падения
mass = float(input("Введите массу объекта в килограммах (кг): "))
height = float(input("Введите высоту в метрах (м): "))
gravity = float(input("Введите ускорение свободного падения (м/с²): "))

# Вычисляем потенциальную энергию
potential_energy = mass * gravity * height

# Выводим результат
print(f"Потенциальная энергия: {potential_energy} Дж")