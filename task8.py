# Запрашиваем у пользователя массу, удельную теплоёмкость и изменение температуры
mass = float(input("Введите массу вещества в килограммах (кг): "))
specific_heat_capacity = float(input("Введите удельную теплоёмкость вещества (Дж/кг·°C): "))
temperature_change = float(input("Введите изменение температуры в градусах Цельсия (°C): "))

# Вычисляем количество теплоты
heat = mass * specific_heat_capacity * temperature_change

# Выводим результат
print(f"Количество теплоты: {heat} Дж")